<div class="text-justify">
    <ul>
      <li><strong>¿Qué es  encadena en línea?</strong></li>
    </ul>
    <p>Encadena en línea, es el primer portal público de demanda del Ecuador.  En <a href="http://www.encadenaenlinea.com">www.encadenaenlinea.com</a> encontraras varios y diversos productos que representan la necesidad  de demanda real de múltiples empresas ecuatorianas. Estas empresas buscan&nbsp; trabajar con proveedores nacionales de los  productos presentados en el portal. Las empresas solicitantes buscan comprar tu  producto, si encontraste un producto que fabricas, o quisieras fabricar, en el  portal, postula ahora mismo. </p>
    <p><strong>Nota:</strong> Encadena  en línea no garantiza al compra.</p>
    <p>Si el producto que haces no es solicitado en  “encadena en línea”, por favor dirígete hacia la pregunta # 7. </p>
    <ul>
      <li><strong>¿Cómo  funciona?</strong></li>
    </ul>
    <p>En  “encadena en línea” encontraras varios y diversos productos actualmente requeridos  en el mercado nacional. Si encuentras uno o varios productos que puedes o  quisieras producir, puedes postular&nbsp; a  través de la página “www.encadenaenlinea.com” y ser considerado como potencial  candidato a suplir este nicho especifico de demanda&nbsp; en el mercado.<strong></strong></p>
    <ul>
      <li><strong>¿Qué es  postular?</strong></li>
    </ul>
    <p>Postular es mostrar interés de  producción por un producto determinado y registrarse con datos personales en el  portal. Para postular, el interesado tiene que ingresar a la página “encadenaenlínea.com”  y escoger el producto por el cual tiene afinidad de producción. Una vez  escogido el producto, el postulante debe seguir con el proceso de registro  llenando los formularios&nbsp; en la  plataforma. Los pasos a seguir se explican en el siguiente <a href="docs/Encadena.pdf" target="_blank">link</a>.</p>
    <ul>
      <li><strong>¿Qué es AEI?</strong></li>
    </ul>
    <p>La AEI es una red de actores públicos,  privados y académicos que busca fomentar el emprendimiento y la innovación a  nivel nacional, como base del desarrollo productivo del Ecuador. El rol de la  AEI es generar continuidad en procesos de proyectos mediante la articulación de  esfuerzos entre aliados, de tal manera que se generen sinergias que permitan  facilitar que los emprendedores e innovadores tengan acceso a: Información de  emprendimiento e innovación, financiamiento de capital de riesgo, educación y  formación emprendedora, espacio de diálogo público-privado, retos de  innovación.</p>
    <ul>
      <li><strong>¿Cómo  trabaja el MIRPO con AEI?</strong></li>
    </ul>
    <p>El MIPRO trabaja  en conjunto con la AEI, apoyando a emprendedores y pequeñas empresas para  desarrollar sus ideas de negocio y ayudándolos a fortalecerse en lo necesario  para poder ingresar a mercados y ser competitivos. La AEI y el MIPRO comparten la  iniciativa principal de fomentar la innovación y el emprendimiento en el Ecuador,  para ultimadamente potenciar &nbsp;la  industria Ecuatoriana. <br>
    </p>
    <ul>
      <li><strong>¿Quién necesita estos productos?</strong></li>
    </ul>
    <p>Aproximadamente  100 empresas ecuatorianas, de varias regiones del país,&nbsp; han publicado sus necesidades reales de  demanda en el portal “encadena en línea”.</p>
    <ul>
      <li><strong>Mi producto no es solicitado en el  portal.&nbsp; ¿Cómo me pueden apoyar? </strong></li>
    </ul>
    <p>Si tienes un producto que no consta en el portal de encadena en  línea y quieres promocionarlo, la AEI te puede ayudar a explorar oportunidades  de acceso a mercados.&nbsp; Visita el  siguiente link: <a href="http://aei.ec/web/xp/productos-innovadores/" target="_blank">http://aei.ec/web/xp/productos-innovadores/</a></p>
    <ul>
      <li><strong>¿Qué clase de productos conforman los  diferentes sectores?</strong></li>
    </ul>
    <p><strong>&nbsp;</strong>En&nbsp; “encadena en línea” encontrarás 8 sectores. A  continuación se detallan algunos grupos de productos comprendidos en los  diferentes sectores:</p>
    <ul>
      <ul>
        <li>Alimentos (Alimentos  frescos &nbsp;y procesados)</li>
        <li>Petroquímica&nbsp;(Plásticos,  neumáticos, cosméticos y más…) </li>
        <li>Construcción&nbsp;(Cerámica  plana, vajilla y más…) </li>
        <li>Confección y calzado (Prendas de vestir, bisutería,  artesanías y más…)</li>
        <li>Metalmecánica&nbsp;(Piezas, componentes, accesorias de metal y  más…)</li>
        <li>Industria Farmacéutica&nbsp;(Vitaminas, tabletas y más…)</li>
        <li>Productos forestales&nbsp;(Muebles, productos de papel y más…)</li>
        <li>Tecnología&nbsp;(Electrodomésticos,  máquinas, accesorios tecnológicos y más…)                            </li>
      </ul>
    </ul>
    <ul>
      <li><strong>¿Cuándo sabré si he sido seleccionado?</strong></li>
    </ul>
    <p>Si has  completado con éxito tu registro y cumples con un porcentaje igual o mayor  al&nbsp; 75% del total de los requisitos  establecidos, recibirás una notificación vía correo electrónico que te indicara  los pasos de la siguiente etapa. Si no logras cumplir con los requisitos  también recibirás un correo indicando las alternativas a seguir para mejorar tu  perfil y/o producto. </p>
    <ul>
      <li><strong>¿A cuántos  productos puedo postular?</strong></li>
    </ul>
    <p>Los  interesados pueden postular a una cantidad ilimitada de productos.&nbsp; Sin embargo, siempre se debe culminar con  éxito el registro de un producto antes de poder proceder a postular al  siguiente.</p>
    <ul>
      <li><strong>¿Qué pasa si  puedo proveer el producto, pero no en las cantidades requeridas?</strong></li>
    </ul>
    <p>Se  articulara con la empresa requirente los términos y condiciones. Es posible que  la empresa acepte tener varios proveedores, sin embargo no existe garantía  alguna.&nbsp; A través del apoyo de la AEI, la  empresa será expuesta a posibilidades de expansión de capacidad productiva.</p>
    <ul>
      <li><strong>¿De ser  escogido, por cuánto tiempo podre ser el proveedor de un producto en cuestión?</strong></li>
    </ul>
    <p>El tiempo  durante el cual el postulante seleccionado podrá proveer sus productos,  dependerá de la duración del contrato que será negociada directamente con la  empresa requirente.</p>
    <ul>
      <li><strong>¿Qué pasa si  varios postulantes cumplen con los requisitos, quien será elegido y en base a  qué?</strong></li>
    </ul>
    <p>Si existen  varios postulantes aptos para proveer, quedara en manos de la entidad  requirente elegir uno o varios de los proveedores en cuestión. La decisión está  sujeta a las políticas de selección&nbsp; y  contratación de proveedores de la empresa solicitante. </p>
    <ul>
      <li><strong>¿Tengo un  producto que es similar, pero no igual al producto buscado?</strong></li>
    </ul>
    <p>Los  sectorialistas del MIPRO conjuntamente con la AEI apoyaran a la empresa con  fines de lograr acoplarse, a &nbsp;medida de  lo posible, a los requerimientos de la empresa solicitante. Es posible que la  empresa interesada opte por aceptar el producto ofertado, sin embargo no existe  garantía.</p>
    <ul>
      <li><strong>¿Cuántos  postulantes habrá por producto antes de que se haga una elección?</strong></li>
    </ul>
    <p>Se realizara una evaluación de los postulantes una vez que se registren  10 empresas oferentes. Si después de evaluar el perfil de dichas empresas, no  se logra encontrar un proveedor adecuado para suplir la demanda en cuestión, el  producto vuelve a ser habilitado en el portal para siguientes postulaciones.</p>
